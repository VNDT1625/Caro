export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<Language>(() => {
    const saved = localStorage.getItem('language') as Language
    return saved || 'vi'
  })

  const setLanguage = (lang: Language) => {
    setLanguageState(lang)
    localStorage.setItem('language', lang)
    document.documentElement.setAttribute('lang', lang)
    
    // Dispatch event Ä‘á»ƒ Ä‘á»“ng bá»™ vá»›i cÃ¡c component khÃ¡c
    window.dispatchEvent(new CustomEvent('languageChanged', { detail: lang }))
  }

  const t = (key: string, params?: Record<string, any>): string => {
    // Attempt order:
    // 1. Exact flat key in current language (legacy map)
    // 2. Exact flat key in English
    // 3. Nested traversal in current language (i18n.json)
    // 4. Nested traversal in English
    // 5. Fallback: last segment THEN full key
    let text: string | undefined

    // 1 & 2: flat lookup even if key contains dots
    const flatCurrent = translations[language]?.[key]
    if (flatCurrent) text = flatCurrent
    if (!text) {
      const flatEn = translations['en']?.[key]
      if (flatEn) text = flatEn
    }

    // 3 & 4: nested lookup only if not found flat
    if (!text && key.includes('.')) {
      const keys = key.split('.')
      const traverse = (lang: Language): string | undefined => {
        let current: any = i18nTranslations[lang]
        for (const k of keys) {
          if (current && typeof current === 'object' && k in current) {
            current = current[k]
          } else {
            return undefined
          }
        }
        return typeof current === 'string' ? current : undefined
      }
      text = traverse(language) || traverse('en')

      if (!text) {
        // 5: fallback last segment; if too generic return full key
        const last = keys[keys.length - 1]
        text = last.length <= 2 ? key : last
      }
    }

    if (!text) {
      // Final fallback if key had no dots
      text = key
    }

    // Parameter replacement
    if (params) {
      Object.keys(params).forEach(param => {
        text = text!.replace(new RegExp(`\\{${param}\\}`, 'g'), String(params[param]))
      })
    }
    return text
  }

  useEffect(() => {
    // Äáº·t ngÃ´n ngá»¯ ban Ä‘áº§u
    document.documentElement.setAttribute('lang', language)
    
    // Láº¯ng nghe sá»± kiá»‡n languageChanged tá»« cÃ¡c component khÃ¡c (nhÆ° Profile settings)
    const handleLanguageChange = (event: CustomEvent) => {
      const newLang = event.detail as Language
      if (newLang && newLang !== language) {
        setLanguageState(newLang)
        localStorage.setItem('language', newLang)
        document.documentElement.setAttribute('lang', newLang)
      }
    }
    
    window.addEventListener('languageChanged', handleLanguageChange as EventListener)
    
    return () => {
      window.removeEventListener('languageChanged', handleLanguageChange as EventListener)
    }
  }, [language])

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  )
}

export function useLanguage() {
  const context = useContext(LanguageContext)
  if (context === undefined) {
    throw new Error('useLanguage must be used within a LanguageProvider')
  }
  return context
}
